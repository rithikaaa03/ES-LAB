__Vectors
		DCD 0x10001000
		DCD Reset_Handler
		ALIGN
		AREA mycode,CODE,READONLY
		ENTRY
		EXPORT Reset_Handler
Reset_Handler
		LDR R0, =NUM
		LDR R3, =RESULT
		LDRB R1,[R0]
		AND R2,R1,#0x0F
		CMP R2,#09
		BCS DOWN
		ADD R2,#07
DOWN	ADD R2,#0x30
		STRB R2,[R3]
		AND R4,R1,#0xF0
		MOV R2,R4,LSR #04
		CMP R4,#09
		BCC DOWN1
		ADD R4,#07
DOWN1	ADD R1,#0x30
		STRB R4,[R3,#01]
STOP 	B STOP
NUM DCD 0xA1	
		AREA mydata,DATA,READWRITE
RESULT DCD 0
		END